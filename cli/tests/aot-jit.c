static size_t params_to_s(
  char * const dst,
  const proto_t proto,
  const pwasm_val_t * const params
) {
  size_t ofs = 0;

  dst[0] = '(';
  ofs += 1;

  for (size_t i = 0; i < proto.num_params; i++) {
    const pwasm_value_type_t type = proto.params[i];

    switch (type) {
    case PWASM_VALUE_TYPE_I32:
      ofs += sprintf(dst + ofs, "%s%u", ((i > 0) ? ", " : ""), params[i].i32);
      break;
    case PWASM_VALUE_TYPE_I64:
      ofs += sprintf(dst + ofs, "%s%lu", ((i > 0) ? ", " : ""), params[i].i64);
      break;
    case PWASM_VALUE_TYPE_F32:
      ofs += sprintf(dst + ofs, "%s%f", ((i > 0) ? ", " : ""), params[i].f32);
      break;
    case PWASM_VALUE_TYPE_F64:
      ofs += sprintf(dst + ofs, "%s%f", ((i > 0) ? ", " : ""), params[i].f64);
      break;
    default:
      // never reached
      errx(EXIT_FAILURE, "unknown value type: %u", type);
    }
  }

  dst[ofs] = ')';
  ofs += 1;

  return ofs;
}

static size_t types_to_s(
  char * const dst,
  const pwasm_value_type_t * const types,
  const size_t num_types
) {
  size_t ofs = 0;
  dst[0] = '[';
  ofs += 1;

  for (size_t i = 0; i < num_types; i++) {
    const char * const name = pwasm_value_type_get_name(types[i]);
    const size_t name_len = strlen(name);

    if (i > 0) {
      dst[ofs] = ',';
      dst[ofs + 1] = ' ';
      ofs += 2;
    }

    memcpy(dst + ofs, name, name_len);
    ofs += name_len;
  }

  dst[ofs] = ']';
  ofs += 1;

  // return length
  return ofs;
}

static size_t proto_to_s(
  char * const dst,
  const proto_t proto
) {
  size_t ofs = 0;
  // append parameter types
  ofs += types_to_s(dst, proto.params, proto.num_params);

  // append arrow
  memcpy(dst + ofs, " -> ", 4);
  ofs += 4;

  // append result types
  ofs += types_to_s(dst + ofs, proto.results, proto.num_results);

  // return length
  return ofs;
}

static size_t get_test_name(
  char * const dst,
  const test_call_t test
) {
  const proto_t proto = TEST_PROTOS[test.type];
  const size_t mod_len = strlen(test.mod);
  const size_t func_len = strlen(test.func);
  size_t ofs = 0;

  // append mod name
  memcpy(dst, test.mod, mod_len);
  ofs += mod_len;

  dst[ofs] = '.';
  ofs += 1;

  // append func name
  memcpy(dst + ofs, test.func, func_len);
  ofs += func_len;

  // append parameters
  ofs += params_to_s(dst + ofs, proto, test.params);

  // append comma
  memcpy(dst + ofs, ", ", 2);
  ofs += 2;

  // append prototype
  ofs += proto_to_s(dst + ofs, proto);

  // terminate
  dst[ofs] = '\0';

  // return name length, excluding null byte
  return ofs;
}

static void check_results(
  cli_test_ctx_t * const test_ctx,
  const cli_test_t * const cli_test,
  const test_call_t test,
  const char * const test_name,
  const pwasm_stack_t * const stack
) {
  char buf[1024];
  char name_buf[512];

  const proto_t proto = TEST_PROTOS[test.type];

  for (size_t i = 0; i < proto.num_results; i++) {
    const pwasm_value_type_t type = proto.results[i];
    const pwasm_val_t got_val = stack->ptr[i];
    const pwasm_val_t exp_val = test.results[i];

    snprintf(name_buf, sizeof(name_buf), "%s: check results[%zu]", test_name, i);

    switch (type) {
    case PWASM_VALUE_TYPE_I32:
      if (got_val.i32 == exp_val.i32) {
        // build pass text
        cli_test_pass(test_ctx, cli_test, name_buf);
      } else {
        // build fail text
        snprintf(buf, sizeof(buf), "%s failed: got 0x%08x, expected 0x%08x", name_buf, got_val.i32, exp_val.i32);
        cli_test_fail(test_ctx, cli_test, buf);
      }

      break;
    case PWASM_VALUE_TYPE_I64:
      if (got_val.i64 == exp_val.i64) {
        // build pass text
        cli_test_pass(test_ctx, cli_test, name_buf);
      } else {
        // build fail text
        snprintf(buf, sizeof(buf), "%s failed: got 0x%016lx, expected 0x%016lx", name_buf, got_val.i64, exp_val.i64);
        cli_test_fail(test_ctx, cli_test, buf);
      }

      break;
    case PWASM_VALUE_TYPE_F32:
      if (NEARLY_EQUAL(got_val.f32, exp_val.f32)) {
        // pass assertion
        cli_test_pass(test_ctx, cli_test, name_buf);
      } else {
        // fail assertion
        snprintf(buf, sizeof(buf), "%s failed: got %f, expected %f", name_buf, got_val.f32, exp_val.f32);
        cli_test_fail(test_ctx, cli_test, buf);
      }

      break;
    case PWASM_VALUE_TYPE_F64:
      if (NEARLY_EQUAL(got_val.f64, exp_val.f64)) {
        // pass assertion
        cli_test_pass(test_ctx, cli_test, name_buf);
      } else {
        // fail assertion
        snprintf(buf, sizeof(buf), "%s failed: got %f, expected %f", name_buf, got_val.f64, exp_val.f64);
        cli_test_fail(test_ctx, cli_test, buf);
      }

      break;
    default:
      // never reached
      snprintf(buf, sizeof(buf), "unknown value type: %u", type);
      cli_test_error(test_ctx, buf);
      return;
    }
  }
}

void test_aot_jit(
  cli_test_ctx_t * const test_ctx,
  const cli_test_t * const cli_test
) {
  // create a memory context
  pwasm_mem_ctx_t mem_ctx = pwasm_mem_ctx_init_defaults(NULL);

  // set up stack
  pwasm_val_t stack_vals[MAX_STACK_DEPTH];
  pwasm_stack_t stack = {
    .ptr = stack_vals,
    .len = MAX_STACK_DEPTH,
  };

  // init jit compiler
  pwasm_jit_t jit;
  if (!pwasm_dynasm_jit_init(&jit, &mem_ctx)) {
    cli_test_error(test_ctx, "pwasm_dynasm_jit_compiler_init() failed");
    return;
  }

  // get aot jit callbacks
  pwasm_env_cbs_t cbs;
  pwasm_aot_jit_get_cbs(&cbs, &jit);

  // create aot jit environment, check for error
  pwasm_env_t env;
  if (!pwasm_env_init(&env, &mem_ctx, &cbs, &stack, NULL)) {
    cli_test_error(test_ctx, "pwasm_env_init() failed");
    return;
  }

  // build buffer
  const pwasm_buf_t buf = { AOT_WASM, sizeof(AOT_WASM) };

  // parse mod, check for error
  pwasm_mod_t mod;
  if (!pwasm_mod_init(&mem_ctx, &mod, buf)) {
    cli_test_error(test_ctx, "pwasm_mod_init() failed");
    return;
  }

  // add mod to env, check for error
  if (!pwasm_env_add_mod(&env, "aot", &mod)) {
    cli_test_error(test_ctx, "pwasm_env_add_mod() failed");
    return;
  }

  for (size_t i = 0; i < LEN(TEST_CALLS); i++) {
    const test_call_t test = TEST_CALLS[i];
    const proto_t proto = TEST_PROTOS[test.type];

    // populate stack
    stack.pos = proto.num_params;
    if (proto.num_params > 0) {
      const size_t num_bytes =  proto.num_params * sizeof(pwasm_val_t);
      memcpy(stack.ptr, test.params, num_bytes);
    }

    char name_buf[512];
    char buf[1024];
    get_test_name(name_buf, test);

    // call function, check result
    const bool call_ok = (pwasm_call(&env, test.mod, test.func) != test.fails);
    if (call_ok) {
      snprintf(buf, sizeof(buf), "%s: call passed", name_buf);
      cli_test_pass(test_ctx, cli_test, buf);
    } else {
      snprintf(buf, sizeof(buf), "%s: call failed", name_buf);
      cli_test_fail(test_ctx, cli_test, buf);
    }

    // check stack size
    if (call_ok && !test.fails) {
      if (stack.pos == proto.num_results) {
        snprintf(buf, sizeof(buf), "%s: stack size == %zu", name_buf, stack.pos);
        cli_test_pass(test_ctx, cli_test, buf);
      } else {
        snprintf(buf, sizeof(buf), "%s: stack size: got %zu, expected %zu", name_buf, stack.pos, proto.num_results);
        cli_test_fail(test_ctx, cli_test, buf);
      }
    } else if (call_ok && test.fails) {
      snprintf(buf, sizeof(buf), "%s: stack size: n/a", name_buf);
      cli_test_pass(test_ctx, cli_test, buf);
    } else {
      snprintf(buf, sizeof(buf), "%s: stack size: call failed", name_buf);
      cli_test_fail(test_ctx, cli_test, buf);
    }

    if (call_ok && !test.fails) {
      check_results(test_ctx, cli_test, test, name_buf, &stack);
    }
  }

  // finalize environment and jit
  pwasm_env_fini(&env);
  pwasm_jit_fini(&jit);
}
